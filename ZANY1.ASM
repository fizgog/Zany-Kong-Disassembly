;-------------------------------------------------------------------------
; Main entry point at address 4300
; Switch to *Tape and relocate game from 1A00 to 0A00
;-------------------------------------------------------------------------
.L3300
        LDA #$8C
        LDX #$00
        LDY #$00
        JSR OSBYTE      ; Select cassette file system and set speed

        ; Relocation code from 1A00 to 0A00
        LDA #$00
        STA temp_address_low
        STA screen_address_low
        LDA #$0A
        STA temp_address_high
        LDA #$1A
        STA screen_address_high
        LDY #$00
.L3319
        LDA (screen_address_low),Y
        STA (temp_address_low),Y
        INY
        BNE L3319

        INC temp_address_high
        INC screen_address_high
        LDA screen_address_high
        CMP #$43
        BNE L3319

        LDA #$E1
        LDX #$00
        JSR OSBYTE          ; Set base number for function-key codes

        JMP L30DD

